package com.bridgelabz.model;

public class StudentDetails {
	
	private String StutentName;
	private int id;
	private long Mobile;
	private String email;
	private String Stream;
	private int sid;
	
	
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public String getStutentName() {
		return StutentName;
	}
	public void setStutentName(String stutentName) {
		StutentName = stutentName;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public long getMobile() {
		return Mobile;
	}
	public void setMobile(long mobile) {
		Mobile = mobile;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getStream() {
		return Stream;
	}
	public void setStream(String stream) {
		Stream = stream;
	}
	
	
	

}
